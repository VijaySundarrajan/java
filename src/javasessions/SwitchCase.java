package javasessions;

public class SwitchCase {

	public static void main(String[] args) {

		String animal = "DoG";
		switch (animal.toLowerCase()) {
		case "cow":
			System.out.println("cow, It gives milk");
			break;
		case "pig":
			System.out.println("pig, It gives flesh");
			break;
		case "cat":
			System.out.println("cat, It drinks milk");
			break;
		case "dog":
			System.out.println("dog, It barks");
			break;

		default:
			System.out.println("plz pass correct name");
			break;
		}

		

	}

	
	}

